package com.JavaOperators;

public class UnaryOperators 
{
    public static void main( String[] args )
    {
    	int num=10;
    	++num;
    	System.out.println(num);
    	System.out.println("===========");
    	num++;
    	System.out.println(num);
    	System.out.println("===========");
    	--num;
    	System.out.println(num);
    	System.out.println("===========");
    	num--;
    	System.out.println(num);
    }
}
